# Numerische Datentypen
a = 5       # Ganzzahl / integer
b = 5.5     # Dezimalzahl / Float
c = 5j      # Komplexe Zahl

print(a, b, c)
print(str(a), str(b), str(c))   #Datenkonvertierung

